    public void submit() {
        Client client = clientManager.findClientById(selectedClientId);
        LibraryItem item = itemManager.findLibraryItemById(Long.valueOf(itemIdField.getText()));

        try {
            borrowManager.createBorrow(client, item);

            JOptionPane.showMessageDialog(addButton, "Successfully added borrow",
                    "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);

        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(addButton, ex.getMessage(),
                        "Error Message", JOptionPane.ERROR_MESSAGE);            
        }
        addButton.setEnabled(false);
    }

