    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        
        if (nameField.getText().isEmpty() || authorField.getText().isEmpty() || isbnField.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(submitButton, "Field must be filled",
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        submit();
    }//GEN-LAST:event_submitButtonActionPerformed
       
    public void submit() {
        Book book = factory.getBean(Book.class);

        book.setName(nameField.getText());
        book.setAuthor(authorField.getText());
        book.setIsbn(isbnField.getText());

        try {
            itemManager.createLibraryItem(book);

            JOptionPane.showMessageDialog(submitButton, "Successfully added book",
                    "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);

        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(submitButton, ex.getMessage(),
                        "Error Message", JOptionPane.ERROR_MESSAGE);
        }
        
        submitButton.setEnabled(false);
    }

