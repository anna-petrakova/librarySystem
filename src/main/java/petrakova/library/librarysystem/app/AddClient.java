    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        
        if (textFieldName.getText().isEmpty() || textFieldSurname.getText().isEmpty() || textFieldEmail.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(Submit, "Field must be filled",
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        submit();

    }//GEN-LAST:event_SubmitActionPerformed
    
    private void submit() {
        Client client = factory.getBean(Client.class);

            client.setFirstName(textFieldName.getText());
            client.setSurname(textFieldSurname.getText());
            client.setEmail(textFieldEmail.getText());

            try {
                clientManager.createClient(client);

                JOptionPane.showMessageDialog(Submit, "Successfully added client",
                        "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);

            } catch (IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(Submit, ex.getMessage(),
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }
            
            Submit.setEnabled(false);
    }   

